package main

import "fmt"

func main() {

	x := 10
	y := 20

	fmt.Println("x:", x, "y:", y)
	fmt.Println("x == y", x == y) //Igual que
	fmt.Println("x != y", x != y) //Diferente de
	fmt.Println("x < y", x < y) //Menor que
	fmt.Println("x <= y", x <= y) //Menor o igual que
	fmt.Println("x > y", x > y) //Mayor
	fmt.Println("x >= y", x >= y) //Mayor o igual que
}